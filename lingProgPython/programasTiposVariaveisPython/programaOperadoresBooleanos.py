# Na ling python, o tipo booleano (bool) é uma especialização do tipo inteiro (int), que pode assumir
# valor verdadeiro ou falso. Verdadeiro -> True == 1; Falso -> False == 0
# Exemplos de valores falsos quando tratados como booleanos: 
# False (falso); None (nulo); 0 (zero); '' (string vazia); [] (lista vazia); () tupla vazia;
# {} (dicionário vazio); outras estruturas com o tamanho igual a zero.
# Logo, os objetos fora opostos aos exemplos são considerados verdadeiros.

print(0 and 3)
print(2 and 3)
print(0 or 3)
print(2 or 3)
print(not 0)
print(not 2)
print(2 in (2, 3))
print(2 == 3)

